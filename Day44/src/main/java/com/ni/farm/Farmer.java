package com.ni.farm;

public class Farmer {
    public String name;
    public int age;
    public float yearsOfExperience;

    public Farmer() {
    }

    public Farmer(String name, int age, float yearsOfExperience) {
        this.name = name;
        this.age = age;
        this.yearsOfExperience = yearsOfExperience;
    }    
}
